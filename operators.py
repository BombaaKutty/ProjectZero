# Arithmetic operators - Simple calculations
num1 = 10
num2 = 3

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2)  # Modulus - Returns the remainder after dividing the two numbers

# Comparison operators - Used to compare values
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)  # Less than or equal to
print(num1 >= num2)  # Greater than or equal to
print(num1 == num2)  # Equal to
print(num1 != num2)  # Not equal to

# Assignment Operators -  Assign values to variables
x = 100
print(x)

x += 2
print(x)
y = 56
y -=3
print(y)


# Logic operators - and, or, not
a = 23
print( a < 40 and a < 2 )
print( a < 40 or a < 2 )
print(not( a < 40 or a < 2 ))


# Operator Precedence - Order in which operations get executed
result = (10+6/2-4)
print(int(result))